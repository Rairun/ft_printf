#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	if (n == -2147483648)
	{
		ft_putnbr(n / 10);
		ft_putnbr(8);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		c = n % 10 + 48;
		ft_putnbr(n / 10);
		write(1, &c, 1);
	}
	else
	{
		c = n + 48;
		write(1, &c, 1);
	}
}
